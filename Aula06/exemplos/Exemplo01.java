package exemplos;

public class Exemplo01 {
    public static void main(String[] args) {
        // ++ variável - soma 1 à variável e depois utilize a variável
        // variável ++ - utilize a variável e depois some 1

        int numero = 5;

        System.out.println(numero++);
        System.out.println(numero);
        
        numero = 5;
        
        System.out.println(++numero);
        System.out.println(numero);
        

    }
}
